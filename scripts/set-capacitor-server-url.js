#!/usr/bin/env node
/**
 * Set or clear the Capacitor live-reload URL for native shells.
 *
 * Why this exists:
 * - Environment variables can be finicky depending on how `npx cap sync` is invoked.
 * - Capacitor config runs in Node, so we can reliably read a local dotenv file.
 *
 * This script writes `.env.capacitor` (ignored by git).
 *
 * Usage:
 *   node scripts/set-capacitor-server-url.js http://localhost:3000
 *   node scripts/set-capacitor-server-url.js --clear
 */

const fs = require('node:fs');
const path = require('node:path');

const args = process.argv.slice(2);
const platformFlagIndex = args.indexOf('--platform');
const platformArg = platformFlagIndex >= 0 ? args[platformFlagIndex + 1] : undefined;
const platform = (platformArg || (args.includes('--ios') ? 'ios' : args.includes('--android') ? 'android' : '')).toLowerCase();
const shouldClear = args.includes('--clear') || args.includes('--unset');
// Prefer an explicit http(s) URL argument. This prevents mistakes when we pass
// `--platform ios http://localhost:3000` (where `ios` is also a non-flag token).
const urlArg = args.find((a) => /^https?:\/\//i.test(String(a || '')));

const envFilePath = path.resolve(__dirname, '..', '.env.capacitor');

function keyForPlatform(p) {
  if (p === 'ios') return 'CAPACITOR_SERVER_URL_IOS';
  if (p === 'android') return 'CAPACITOR_SERVER_URL_ANDROID';
  return 'CAPACITOR_SERVER_URL';
}

function parseEnvFile(content) {
  const map = new Map();
  const lines = String(content || '').split(/\r?\n/);
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const m = trimmed.match(/^([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*)$/);
    if (!m) continue;
    map.set(m[1], m[2] || '');
  }
  return map;
}

function serializeEnvFile(map) {
  const header = [
    '# Auto-generated by npm scripts. Do not commit this file.',
    '# Controls Capacitor native shell live reload (server.url).',
    '# Platform-specific keys prevent Android emulator URLs (10.0.2.2) from breaking iOS builds.',
    ''
  ];

  const lines = [];
  const orderedKeys = [
    'CAPACITOR_LIVE_RELOAD',
    'CAPACITOR_SERVER_URL_IOS',
    'CAPACITOR_SERVER_URL_ANDROID',
    'CAPACITOR_SERVER_URL'
  ];
  for (const k of orderedKeys) {
    const v = map.get(k);
    if (v) lines.push(`${k}=${v}`);
  }

  return header.concat(lines).concat(['']).join('\n');
}

function upsertEnvKey(key, url) {
  const existing = fs.existsSync(envFilePath) ? fs.readFileSync(envFilePath, 'utf8') : '';
  const map = parseEnvFile(existing);
  // Any time we set a URL, opt in to live reload.
  map.set('CAPACITOR_LIVE_RELOAD', '1');
  map.set(key, url);

  // Safety: if we are setting a platform-specific URL, remove the generic key so
  // iOS doesn't accidentally inherit Android's emulator host mapping (10.0.2.2)
  // or a public domain from a previous run.
  if (key !== 'CAPACITOR_SERVER_URL') {
    map.delete('CAPACITOR_SERVER_URL');
  }
  fs.writeFileSync(envFilePath, serializeEnvFile(map), 'utf8');
}

function clearEnvKey(key) {
  if (!fs.existsSync(envFilePath)) return;
  const existing = fs.readFileSync(envFilePath, 'utf8');
  const map = parseEnvFile(existing);
  map.delete(key);

  // If there are no URL keys left, also disable live reload.
  const hasAnyUrl = ['CAPACITOR_SERVER_URL_IOS', 'CAPACITOR_SERVER_URL_ANDROID', 'CAPACITOR_SERVER_URL'].some((k) => map.get(k));
  if (!hasAnyUrl) {
    map.delete('CAPACITOR_LIVE_RELOAD');
  }

  // If no keys remain, remove the file entirely.
  if (!hasAnyUrl) {
    fs.unlinkSync(envFilePath);
    return;
  }
  fs.writeFileSync(envFilePath, serializeEnvFile(map), 'utf8');
}

function clearEnvFile() {
  if (fs.existsSync(envFilePath)) {
    fs.unlinkSync(envFilePath);
  }
}

if (shouldClear) {
  // If a platform is specified, clear only that platform's URL.
  // Otherwise, clear the entire file (legacy behavior).
  if (platform === 'ios' || platform === 'android') {
    clearEnvKey(keyForPlatform(platform));
  } else {
    clearEnvFile();
  }
  process.exit(0);
}

const url = String(urlArg || '').trim();
if (!url) {
  // If no URL provided, treat it as a clear.
  clearEnvFile();
  process.exit(0);
}

// Minimal validation: allow http(s) URLs only.
if (!/^https?:\/\//i.test(url)) {
  console.error('CAPACITOR_SERVER_URL must start with http:// or https://');
  process.exit(2);
}

upsertEnvKey(keyForPlatform(platform), url);
process.exit(0);
