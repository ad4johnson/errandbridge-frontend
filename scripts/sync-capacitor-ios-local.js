#!/usr/bin/env node

const { spawnSync } = require("node:child_process");
const fs = require("node:fs");
const os = require("node:os");
const path = require("node:path");

function isPrivateIpv4(address) {
	if (!/^\d{1,3}(\.\d{1,3}){3}$/.test(String(address || ""))) return false;
	return (
		address.startsWith("10.") ||
		address.startsWith("192.168.") ||
		/^172\.(1[6-9]|2\d|3[0-1])\./.test(address)
	);
}

function scoreInterface(name = "") {
	const key = String(name || "").toLowerCase();
	if (key === "en0") return 100;
	if (key === "en1") return 95;
	if (key.startsWith("en")) return 90;
	if (key.startsWith("bridge")) return 80;
	if (key.startsWith("utun")) return 10;
	if (key.startsWith("lo")) return 0;
	return 50;
}

function detectCurrentLanIp() {
	const interfaces = os.networkInterfaces();
	const candidates = [];

	for (const [name, entries] of Object.entries(interfaces)) {
		for (const entry of entries || []) {
			if (!entry || entry.internal || entry.family !== "IPv4") continue;
			if (!isPrivateIpv4(entry.address)) continue;
			candidates.push({
				name,
				address: entry.address,
				score: scoreInterface(name),
			});
		}
	}

	candidates.sort((a, b) => b.score - a.score || a.name.localeCompare(b.name));
	return candidates[0] || null;
}

function run(command, args, env) {
	const result = spawnSync(command, args, {
		stdio: "inherit",
		env,
		shell: false,
	});
	if (result.status !== 0) {
		throw new Error(`${command} ${args.join(" ")} failed with exit code ${result.status || 1}`);
	}
}

function buildCapacitorEnvContent({ resolvedBase, forceLocalhost }) {
	const iosBase = resolvedBase;
	const androidBase = forceLocalhost ? "http://10.0.2.2:8001" : iosBase;
	const useDeviceApi = forceLocalhost ? "false" : "true";

	return [
		"# Auto-generated by scripts/sync-capacitor-ios-local.js",
		"# This file is rewritten for deterministic native local testing.",
		"# Re-run the sync command whenever your LAN IP changes.",
		`REACT_APP_FORCE_API_BASE=true`,
		`REACT_APP_FORCED_API_BASE=${iosBase}`,
		`REACT_APP_USE_DEVICE_API=${useDeviceApi}`,
		`REACT_APP_DEVICE_API_BASE=${iosBase}`,
		`REACT_APP_CAPACITOR_USE_HOSTED_API=false`,
		`REACT_APP_CAPACITOR_API_BASE_IOS=${iosBase}`,
		`REACT_APP_CAPACITOR_API_BASE_ANDROID=${androidBase}`,
		`REACT_APP_API_BASE=${iosBase}`,
		`REACT_APP_API_BASE_URL=${iosBase}`,
		`REACT_APP_LOCAL_API_BASE=${iosBase}`,
		`REACT_APP_GRAPHQL_ENDPOINT=${iosBase}/graphql`,
		"",
	].join("\n");
}

function withTemporaryProductionLocalEnv(content, callback) {
	const envFilePath = path.resolve(__dirname, "..", ".env.production.local");
	const hadOriginal = fs.existsSync(envFilePath);
	const originalContent = hadOriginal ? fs.readFileSync(envFilePath, "utf8") : null;

	fs.writeFileSync(envFilePath, content, "utf8");

	try {
		return callback();
	} finally {
		if (hadOriginal) {
			fs.writeFileSync(envFilePath, originalContent, "utf8");
		} else if (fs.existsSync(envFilePath)) {
			fs.unlinkSync(envFilePath);
		}
	}
}

const args = new Set(process.argv.slice(2));
const printOnly = args.has("--print-only") || args.has("--dry-run");
const forceLocalhost = args.has("--localhost") || args.has("--simulator");
const candidate = forceLocalhost ? null : detectCurrentLanIp();
const resolvedBase = forceLocalhost
	? "http://localhost:8001"
	: candidate
		? `http://${candidate.address}:8001`
		: "http://localhost:8001";

if (printOnly) {
	console.log(resolvedBase);
	process.exit(0);
}

if (candidate) {
	console.log(
		`[cap:sync:ios] Using ${candidate.address} from ${candidate.name} for local backend access.`,
	);
} else {
	if (forceLocalhost) {
		console.log("[cap:sync:ios] Forcing http://localhost:8001 (simulator/loopback mode). ");
	} else {
		console.warn(
			"[cap:sync:ios] No private LAN IPv4 detected; falling back to http://localhost:8001.",
		);
	}
}

const env = {
	...process.env,
	REACT_APP_FORCE_API_BASE: "true",
	REACT_APP_FORCED_API_BASE: resolvedBase,
	REACT_APP_USE_DEVICE_API: forceLocalhost ? "false" : "true",
	REACT_APP_DEVICE_API_BASE: resolvedBase,
	REACT_APP_CAPACITOR_USE_HOSTED_API: "false",
	REACT_APP_CAPACITOR_API_BASE_IOS: resolvedBase,
	REACT_APP_API_BASE: resolvedBase,
	REACT_APP_API_BASE_URL: resolvedBase,
	REACT_APP_LOCAL_API_BASE: resolvedBase,
	REACT_APP_GRAPHQL_ENDPOINT: `${resolvedBase}/graphql`,
};

const tempEnvContent = buildCapacitorEnvContent({ resolvedBase, forceLocalhost });

try {
	withTemporaryProductionLocalEnv(tempEnvContent, () => {
		run("npm", ["run", "build"], env);
		run("npx", ["cap", "sync", "ios"], env);
	});
} catch (error) {
	console.error(`[cap:sync:ios] ${error.message}`);
	process.exit(1);
}
